                                <h1><span class="header-section-number">4</span> Discussion and future research<a href="https://6229.freelancedeveloper.site/discussion-and-future-research#discussion-and-future-research" class="anchor-section" aria-label="Anchor link to header"></a></h1>
                                <p>In our review, we assessed the impact of a wide range of factors on HPG. We summarise our findings from the literature in Table 8. In our review, we highlighted the shortcomings of the literature and the importance for policy driven robust economic analysis of the factors of HPG. To achieve this, it is required to have a clear definition of policy objectives, for example to maximise the wellbeing of New Zealanders, and a clear recognition of the associated economywide trade-offs. It is also critical to have a comprehensive economic assessment framework accounting for inter-relationships between the factors of demand and supply, and the urban development, environmental and economywide outcomes.</p>
                                <div id="table-8-summary-of-literature-review" class="section level3 unlisted unnumbered hasAnchor">
                                    <h3>Table 8 Summary of literature review<a href="https://6229.freelancedeveloper.site/discussion-and-future-research#table-8-summary-of-literature-review" class="anchor-section" aria-label="Anchor link to header"></a></h3>
