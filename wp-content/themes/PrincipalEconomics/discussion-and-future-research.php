                                                impact of availability of
                                                finance in New Zealand.</td>
                                        </tr>
                                        <tr class="even">
                                            <td>Tax policy, housing subsidies
                                                and other interventions</td>
                                            <td><p>There is high agreement that a
                                                    tax policy leads to lower
                                                    speculation incentives and
                                                    therefore lower HPG.</p>
                                                <p>The available literature does
                                                    not provide enough
                                                    evidence-based analysis of the
                                                    likely impact of the tax and
                                                    subsidy policies.</p>
                                                <p>The size of the impact of a tax
                                                    policy on HPG is unclear.</p></td>
