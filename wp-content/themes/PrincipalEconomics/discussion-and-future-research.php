                                                    housing affordability and HPG.</p>
                                                <p>The OCR, however, has a wide
                                                    impact on different sectors of
                                                    the economy, and is not
                                                    considered as an appropriate
                                                    tool for controlling house
                                                    prices.</p>
                                                <p>The impact of monetary policy
                                                    highly depends on the other
                                                    (supply) factors.</p>
                                                <p>The macroeconomic models and the
                                                    models of housing market have
                                                    not been linked.</p></td>
                                        </tr>
