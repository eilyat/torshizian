                                                    conversation about the
                                                    interactions between
                                                    construction costs and all other
                                                    related factors, such as changes
                                                    in technology, the impact of
                                                    planning regulations and the
                                                    scale of the construction
                                                    sector.</p></td>
                                        </tr>
                                        <tr class="odd">
                                            <td>Affordability</td>
                                            <td><p>Overall increase affordability
                                                    has led to HPG.</p>
                                                <p>These indicators of housing
                                                    affordability provide useful
                                                    high-level description of the
                                                    housing affordability issue that
                                                    can be used for a general policy
                                                    discussion. The indicators are
                                                    not useful for providing
                                                    information about the drivers of
                                                    housing affordability.</p>
