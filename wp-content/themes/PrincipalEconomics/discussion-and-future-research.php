                                    <div id="source-principal-economics.-4" class="section level5 unlisted unnumbered hasAnchor">
                                        <h5>Source: Principal Economics.<a href="https://6229.freelancedeveloper.site/discussion-and-future-research#source-principal-economics.-4" class="anchor-section" aria-label="Anchor link to header"></a></h5>
                                        <p>For our review of the available housing models in New Zealand, we contacted the Reserve Bank, the Treasury and the Ministry of Housing and Urban Development (HUD) and asked about their available models. Our review of the information available to us suggests that the available models do not provide any extensive tool for forecasting house prices in New Zealand. Particularly, house prices are mostly an input to the rest of macroeconomic models used by the RBNZ and the Treasury. Both entities used expert judgement (outside the modelling framework) to provide forecasts of house prices. We did not find any information about the performance of the combined models and expert judgement over time. The available tools are not suitable for the assessment of housing policies. We are not sure if the available tools provide a robust framework for the assessment of macroprudential policies. For a policy targeting the wellbeing of New Zealand population, it is critical to assess the distributional impacts of policies. Our review suggests that the available tools do not provide information about distributional impacts of policies.</p>
                                        <p>Policies often aim at improving a housing affordability measure (as their policy target). Housing affordability measures, however, do not provide information on the concept of housing affordability for different types of people. All current measures of housing affordability are flawed both conceptually and empirically (see for example Leishman &amp; Rowley, 2012; Stone, 2006). This leads to incorrect policy analyses especially for people who are not the archetypal pakeha couple with kids. For a robust discussion of housing affordability, we suggest a future study to investigate performance of housing affordability measures versus different population groups’ perception of housing affordability and provides a better housing affordability measure for research and policy.</p>
