                                                    death rates, increases demand
                                                    for housing and leads to HPG.</p>
                                                <p>The cycle in factors of
                                                    migration and house price growth
                                                    leads to complexities in causal
                                                    inference about the impact of
                                                    population growth on house
                                                    prices.</p></td>
                                        </tr>
                                        <tr class="even">
                                            <td>Household size</td>
                                            <td><p>The decrease in the size of
                                                    households and the move towards
